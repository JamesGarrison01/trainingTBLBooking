DROP TABLE REGISTRATION_DETAILS;
DROP TABLE Login_MST;
DROP TABLE tour_plan_mst;

CREATE TABLE tour_plan_mst(
	tour_id NUMBER(6) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	tour_name VARCHAR(20) NOT NULL,
	starting_location VARCHAR(20),
	places_included VARCHAR(20) NOT NULL,
	tour_cost NUMBER(10) NOT NULL,
	discount_per NUMBER(5),
	tour_days NUMBER(5) NOT NULL
);

CREATE TABLE login_mst(
	user_id number(6) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	login_id VARCHAR(20) NOT NULL,
	password VARCHAR(20) NOT NULL,
	user_name VARCHAR(15),
	user_type VARCHAR(10)
);

CREATE TABLE REGISTRATION_DETAILS(
	tour_booking_id number(6) PRIMARY KEY,
	member_name varchar(30) NOT NULL,
	no_of_passengers varchar(20),
	amount_paid number(6) NOT NULL,
	journey_start_date DATE NOT NULL,
	tour_id number(6) NOT NULL,
	isActive number(1) NOT NULL,
	remarks varchar(50),
	CONSTRAINT fk_tour_Id FOREIGN KEY (tour_id)
	REFERENCES Tour_Plan_MST(tour_id)
);

